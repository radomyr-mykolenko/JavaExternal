public class VisualParameters {
    private String stem_color;
    private String leaves_color;
    private int average_height;

    public String getStem_color() {
        return stem_color;
    }

    public void setStem_color(String stem_color) {
        this.stem_color = stem_color;
    }

    public String getLeaves_color() {
        return leaves_color;
    }

    public void setLeaves_color(String leaves_color) {
        this.leaves_color = leaves_color;
    }

    public int getAverage_height() {
        return average_height;
    }

    public void setAverage_height(int average_height) {
        this.average_height = average_height;
    }
}
